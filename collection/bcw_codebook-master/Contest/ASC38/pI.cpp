double freq[MAXC];
double f2[MAXD*MAXD];
vector<int> nonz;
int invnonz[MAXC];

void pre()
{
	double tot = 0;
	for(int i=0; i<MAXC; i++)
		tot += scnt[i];

	for(int i=0; i<MAXC; i++)
		freq[i] = scnt[i] / tot;

	for(int i=0; i<MAXC; i++)
		if(scnt[i])
			nonz.PB(i);
	for(int i=0; i<MAXD; i++)
		invnonz[nonz[i]] = i;

	for(int i=0; i<MAXD; i++)
	{
		int to = 0;
		for(int j=0; j<MAXD; j++)
			to += scnt2[nonz[i]][nonz[j]];
		if(to)
		{
			for(int j=0; j<MAXD; j++)
				f2[i*MAXD+j] = scnt2[nonz[i]][nonz[j]] / (double) to;
		}
	}
}

double cross_entropy(double v1[], vector<double> &v2)
{
	int sz = v2.size();
	double res = 0;
	for(int i=0; i<sz; i++)
	{
		res += v1[i] * (v2[i]<EPS ? INF : -log(v2[i]));
	}
	return res;
}

double calc_xor(int m)
{
	double res = 0;
	for(int i=0; i<m; i++)
	{
		vector<double> vec, vec2;
		vec.resize(MAXC);
		vec2.resize(MAXC);
		double tot = 0;
		for(int j=0; j<MAXC; j++)
		{
			tot += 1;
			vec[j] += 1;
		}
		for(int j=i; j<N; j+=m)
		{
			int c = s[j];
			tot += 1;
			vec[c] += 1;
		}
		for(int j=0; j<MAXC; j++)
			vec[j] /= tot;

		int bst = 0;
		for(int j=0; j<MAXC; j++)
			if(vec[j] > vec[bst])
				bst = j;

		int x = bst ^ 32;
		for(int j=0; j<MAXC; j++)
			vec2[j^x] = vec[j];

		double ent = cross_entropy(freq, vec2);
		res += ent;

		for(int j=i; j<N; j+=m)
			s2[j] = s[j] ^ x;
	}

	res /= m;
	return res;
}

double trans[20][20][MAXD][MAXD];
double trent[20][20];

double calc_perm(int n)
{
	int K = N / n;
	for(int i=0; i<n; i++)
		for(int j=0; j<n; j++)
			for(int x=0; x<MAXD; x++)
				for(int y=0; y<MAXD; y++)
					trans[i][j][x][y] = 1;

	for(int k=0; k<K; k++)
	{
		for(int i=0; i<n; i++)
		{
			for(int j=0; j<n; j++)
			{
				if(i == j) continue;
				int c1 = invnonz[(int)s2[k*n+i]];
				int c2 = invnonz[(int)s2[k*n+j]];
				trans[i][j][c1][c2] += 21;
			}
		}
	}

	for(int i=0; i<n; i++)
	{
		for(int j=0; j<n; j++)
		{
			if(i == j) continue;
			for(int x=0; x<MAXD; x++)
			{
				int tot = 0;
				for(int y=0; y<MAXD; y++)
					tot += trans[i][j][x][y];
				for(int y=0; y<MAXD; y++)
					trans[i][j][x][y] /= tot;
			}
			vector<double> vec;
			for(int x=0; x<MAXD; x++)
				for(int y=0; y<MAXD; y++)
					vec.PB(trans[i][j][x][y]);
			trent[i][j] = cross_entropy(f2, vec) + 15./n;
		}
	}

	vector<int> bperm;
	bool used[n];

	double bcost = INF;
	for(int i=0; i<n; i++)
	{
		vector<int> perm;
		double cost = 0;
		FZ(used);
		int p = i;
		used[p] = true;
		perm.PB(p);
		for(int q=0; q<n-1; q++)
		{
			int nxt = -1;
			for(int j=0; j<n; j++)
			{
				if(used[j]) continue;
				if(nxt == -1 || trent[p][j] < trent[p][nxt])
					nxt = j;
			}
			cost += trent[p][nxt];
			perm.PB(nxt);
			p = nxt;
			used[nxt] = true;
		}
		cost /= (n-1);

		if(cost < bcost)
		{
			bcost = cost;
			bperm = perm;
		}
	}

	//cout<<"N = "<<n<<endl;
	//for(int i=0; i<n; i++){for(int j=0; j<n; j++) cout<<(int)trent[i][j]-200<<" ";cout<<endl;}

	for(int k=0; k<K; k++)
	{
		for(int i=0; i<n; i++)
			s3[k*n+i] = s2[k*n+bperm[i]];
	}
	for(int i=K*n; i<N; i++)
		s3[i] = s2[i];

	//cout<<N<<" "<<bcost<<" "<<bperm<<endl;
	//for(int i=0; i<50; i++) cout<<s3[i];cout<<endl;
	return bcost;
}

