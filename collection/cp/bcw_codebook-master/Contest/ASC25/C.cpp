void fft(int n, int a[], bool iv=false)
{
	int theta = 1;
	for (int m = Z; m >= 2; m >>= 1) {
		int mh = m >> 1;
		for (int i = 0; i < mh; i++) {
			int w = omega[iv ? Z-(i*theta%Z) : i*theta%Z];
			for (int j = i; j < n; j += m) {
				int k = j + mh;
				int x = (a[j] - a[k] + M);
				if (x >= M) x -= M;
				(a[j] += a[k]);
				if (a[j] >= M) a[j] -= M;
				a[k] = w * x % M;
			}
		}
		theta = (theta * 2) % Z;
	}
	int i = 0;
	for (int j = 1; j < n - 1; j++) {
		for (int k = n >> 1; k > (i ^= k); k >>= 1);
		if (j < i) swap(a[i], a[j]);
	}
	if (iv)
		for (i = 0; i < n; i++)
			(a[i] *= inv(n)) %= M;
}

